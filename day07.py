xmax = len(data[0])
beams_x = {data[0].find('S')}

splits = 0
for row in data:
    newbeams = set()
    for x in beams_x:
        if row[x] == '^':
            splits += 1
            if x+1 <= xmax: newbeams.add(x+1)
            if x-1 >= 0: newbeams.add(x-1)
        else: newbeams.add(x)
    beams_x = newbeams

